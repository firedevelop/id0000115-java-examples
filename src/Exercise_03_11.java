public class Exercise_03_11 {
    public static void main(String[] args) {
        for(int i=1;i<=10;i++){
            System.out.println("\nMultiplication table of: " + i );
            for(int j=0; j<=10;j++) {

                System.out.println(i + " x " + j + " = " + i * j);
            }
        }


    }
}